
cup = input("Choose your cup size (S, M, L): ").lower()

if cup == "s":
    print("You have selected a Small cup. The price is $2.")
elif cup == "m":
    print("You have selected a Medium cup. The price is $3.")
elif cup == "l":
    print("You have selected a Large cup. The price is $4.")
else:
    print("Invalid cup size selected. Please choose S, M, or L.")




